	<h2>How to Care for Your Device</h2>
	
	<ul class="blt">
	<li>Shut down the computer, disconnect the power adapter and any peripherals.</li>
	<li>Use a damp, soft, lint-free cloth to clean the computer’s exterior. Avoid getting moisture in any openings. NEVER spray liquid directly on the computer.</li>
	<li>Don’t use aerosol sprays, solvents, or abrasives.</li> 
	<li>MAGIC ERASERS are actually somewhat abrasive so just barely dampen them and gently wipe, do not rub, across the keyboard and wrist rest. </li>
	<li>Wipe gently with a damp Clorox wipe and then follow with a soft, lint-free cloth to dry off any surface after cleaning it.</li>
	</ul>
